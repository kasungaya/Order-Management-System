private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// TODO add your handling code here:
    try {
        File f = jFileChooser1.getSelectedFile();
        String path = f.getAbsolutePath();
        path = path.replace("\\", "/");

        int x = JOptionPane.showConfirmDialog(rootPane, "Do you want to save?");
        if (x == 0) {
            Connection c = DB.mycon();
            Statement s = c.createStatement();
            validation v = new validation();
           
            if(v.isValidName(txtname.getText())== !false && v.isValidPhoneNumber(txttele.getText())==!false && v.isValidEmail(txtemail.getText())== !false && v.isValidID(txtnic.getText())== !false){
            s.executeUpdate("INSERT INTO customer VALUES ('" + txtid.getText() + "','" + txtname.getText() + "','" + txtadd.getText() + "','" + txttele.getText() + "','" + txtemail.getText() + "','" + txtnic.getText() + "','" + cmbtype.getSelectedItem().toString() + "','" + path + "')");
            txtid.setText("");
            txtname.setText("");
            txtadd.setText("");
            txttele.setText("");
            txtemail.setText("");
            txtnic.setText("");
            cmbtype.setSelectedIndex(0);
            lblimg.setIcon(null);
            JOptionPane.showMessageDialog(rootPane, "Successfully Saved");
        } else if (x == 1) {
            txtid.setText("");
            txtname.setText("");
            txtadd.setText("");
            txttele.setText("");
            txtemail.setText("");
            txtnic.setText("");
            cmbtype.setSelectedIndex(0);
            lblimg.setIcon(null);
        }

    } }catch (Exception e) {
        e.printStackTrace();
    }


}//GEN-LAST:event_jButton2ActionPerformed

