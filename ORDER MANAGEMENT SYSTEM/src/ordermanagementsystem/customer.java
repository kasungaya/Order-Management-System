private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
// TODO add your handling code here:
    try {
        Connection c = DB.mycon();
        Statement s = c.createStatement();
        ResultSet rs = s.executeQuery("SELECT * FROM customer WHERE id='" + txtid.getText() + "'");
        while (rs.next()) {
            txtname.setText(rs.getString("name"));
            txtadd.setText(rs.getString("address"));
            txttele.setText(rs.getString("telephone"));
            txtemail.setText(rs.getString("email"));
            txtnic.setText(rs.getString("nic"));
            cmbtype.setSelectedItem(rs.getString("type"));

            File f1 = new File(rs.getString("image"));

            Image im = ImageIO.read(f1);
            im = im.getScaledInstance(lblimg.getWidth(), lblimg.getHeight(), Image.SCALE_SMOOTH);
            lblimg.setIcon(new ImageIcon(im));

        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jButton3ActionPerformed

